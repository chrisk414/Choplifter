/**
 * Sprite PNG generator
 * Reads sprite definitions from SpriteData and outputs PNGs to public/assets/sprites/
 * Run: node tools/generate-sprites.mjs
 */
import sharp from 'sharp';
import { mkdirSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUT_DIR = join(__dirname, '..', 'public', 'assets', 'sprites');
mkdirSync(OUT_DIR, { recursive: true });

// Color palette matching the retro Choplifter style
const PALETTE = {
  '.': null,            // transparent
  '0': [0x00, 0x00, 0x00, 255],  // black (outlines)
  '1': [0x2d, 0x5a, 0x1e, 255],  // dark green
  '2': [0x4a, 0x8c, 0x2a, 255],  // green (heli body)
  '3': [0x6a, 0xb8, 0x3a, 255],  // light green
  '4': [0x55, 0x88, 0xbb, 255],  // cockpit glass
  '5': [0x88, 0xcc, 0xee, 255],  // cockpit highlight
  '6': [0x33, 0x33, 0x33, 255],  // dark gray
  '7': [0x66, 0x66, 0x66, 255],  // gray
  '8': [0xd4, 0xa5, 0x74, 255],  // skin tone
  '9': [0xee, 0xee, 0xee, 255],  // white/light
  'a': [0x8b, 0x69, 0x14, 255],  // brown
  'b': [0x5a, 0x44, 0x10, 255],  // dark brown
  'c': [0xcc, 0x33, 0x33, 255],  // red
  'd': [0xff, 0xcc, 0x00, 255],  // yellow
  'e': [0xff, 0x88, 0x00, 255],  // orange
  'f': [0x44, 0x44, 0x44, 255],  // medium gray
};

function createSprite(pixels, scale = 2) {
  const srcH = pixels.length;
  const srcW = pixels[0].length;
  const w = srcW * scale;
  const h = srcH * scale;
  const buf = Buffer.alloc(w * h * 4, 0); // RGBA, all transparent

  for (let sy = 0; sy < srcH; sy++) {
    for (let sx = 0; sx < srcW; sx++) {
      const ch = pixels[sy][sx];
      const color = PALETTE[ch];
      if (!color) continue; // transparent
      for (let dy = 0; dy < scale; dy++) {
        for (let dx = 0; dx < scale; dx++) {
          const px = sx * scale + dx;
          const py = sy * scale + dy;
          const off = (py * w + px) * 4;
          buf[off] = color[0];
          buf[off + 1] = color[1];
          buf[off + 2] = color[2];
          buf[off + 3] = color[3];
        }
      }
    }
  }
  return { buf, w, h };
}

async function saveSprite(name, pixels, scale = 2) {
  const { buf, w, h } = createSprite(pixels, scale);
  const path = join(OUT_DIR, `${name}.png`);
  await sharp(buf, { raw: { width: w, height: h, channels: 4 } })
    .png()
    .toFile(path);
  console.log(`  ✓ ${name}.png (${w}x${h})`);
}

