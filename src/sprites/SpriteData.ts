// Sprite definitions as pixel arrays
// Each sprite is a 2D array of palette color indices (hex chars)
// '.' = transparent, '0'-'9' and 'a'-'f' = palette indices

export const PALETTE: Record<string, number> = {
  '.': -1,          // transparent
  '0': 0x000000,    // black (outlines)
  '1': 0x2d5a1e,    // dark green (heli body dark)
  '2': 0x4a8c2a,    // green (heli body)
  '3': 0x6ab83a,    // light green (heli highlight)
  '4': 0x5588bb,    // cockpit glass
  '5': 0x88ccee,    // cockpit glass highlight
  '6': 0x333333,    // dark gray (mechanical)
  '7': 0x666666,    // gray (rotor, metal)
  '8': 0xd4a574,    // skin tone
  '9': 0xeeeeee,    // white/light (hostage clothes)
  'a': 0x8b6914,    // brown (barracks)
  'b': 0x5a4410,    // dark brown
  'c': 0xcc3333,    // red
  'd': 0xffcc00,    // yellow (bullet)
  'e': 0xff8800,    // orange (explosion)
  'f': 0x444444,    // medium gray
};

export interface SpriteDefinition {
  width: number;
  height: number;
  pixels: string[];
  scale?: number;
}

